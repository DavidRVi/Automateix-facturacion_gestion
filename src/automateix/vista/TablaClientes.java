    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        String DNI = null;
        if(row!= -1)
        {
            DNI = (String) table.getValueAt(row,0);
            if (JOptionPane.showConfirmDialog(rootPane, "¿Desea realmente eliminar al cliente con DNI '"+ DNI +"'? \nRecuerda que todas las visitas registradas asociadas a este cliente también se borrarán.",
                    "Eliminar Cliente", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
            {

                ControladorEliminarClientes controller = new ControladorEliminarClientes(DNI);
                controller.eliminar();
                Llenartabla(jTextField1.getText());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

