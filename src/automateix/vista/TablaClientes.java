    public void Llenartabla(String data)  {

        PruebaTabla pt = new PruebaTabla(data);
        jPanel2.setLayout(new BorderLayout());
        table = pt.getTable();
     
        if(scrollPane == null)
        {
            scrollPane = new JScrollPane(pt.getTable());

            jPanel2.add(scrollPane, BorderLayout.CENTER);
            jPanel2.setPreferredSize(new Dimension(724, 450));
            jPanel2.repaint();
           
        }
        else
        {
            jPanel2.remove(scrollPane);
            scrollPane = new JScrollPane(pt.getTable());
            jPanel2.add(scrollPane, BorderLayout.CENTER);
            jPanel2.repaint();
        }
         this.pack();
        
      
    }

