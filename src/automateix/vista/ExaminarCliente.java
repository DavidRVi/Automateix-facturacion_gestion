    private void rellenarVisitas(){
        TablaExaminarCliente tec = new TablaExaminarCliente(cliente.getDNI());
        jPanel1.setLayout(new BorderLayout());
        table = tec.getTable();
        
        if(scrollPane == null)
        {
            scrollPane = new JScrollPane(tec.getTable());

            jPanel1.add(scrollPane, BorderLayout.CENTER);
            jPanel1.setPreferredSize(new Dimension(128, 450));
            jPanel1.repaint();
           
        }
        else
        {
            jPanel1.remove(scrollPane);
            scrollPane = new JScrollPane(tec.getTable());
            jPanel1.add(scrollPane, BorderLayout.CENTER);
            jPanel1.repaint();
        }
         this.pack();
        
    }

