    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int tiempo_entrada = 0;
        tiempo_entrada += (int) jSpinner1.getValue();
        tiempo_entrada = tiempo_entrada * 60 + (int) jSpinner2.getValue();
        int tiempo_salida = 0;
        tiempo_salida += (int) jSpinner3.getValue();
        tiempo_salida = tiempo_salida * 60 + (int) jSpinner4.getValue();
        int tiempo_total = 0;
        
        if(tiempo_salida > tiempo_entrada)
        {
            tiempo_total = tiempo_salida - tiempo_entrada;
            jLabel18.setText(Integer.toString(tiempo_total) + " minutos.");
            tablaArticulos.getFactura().addArticulo(new Articulo("ALQUILER ZONA-MINUTO", cf.getCurrentFacturaMinuto(), tiempo_total));
            updateTable();
        }
        else JOptionPane.showMessageDialog(this, "No se puede salir antes de entrar!!!", "Error en los tiempos", JOptionPane.ERROR_MESSAGE);
        
        //updateTable();
    }//GEN-LAST:event_jButton1ActionPerformed

