    private void updatePrecios(){
        DecimalFormat formato = new DecimalFormat("######.##");
        formato.setRoundingMode(RoundingMode.CEILING);
        float discount = 0.0f;
        double totalPrice = tablaArticulos.getFactura().getPrecioTotal();//Double.valueOf(jLabel20.getText());
        
        {
            ivavalue = cf.getCurrentIVA();
            double valor = (double) ivavalue  /100;
            ivaAdded = totalPrice * valor;
            jLabel27.setText(formato.format(ivaAdded));
            //totalPrice = totalPrice + (totalPrice * valor);
            jLabel20.setText(formato.format(totalPrice - ivaAdded));
        }
        if(jCheckBox1.isSelected())
        {
            
            discount =  (int) jSpinner5.getValue() / 100.0f;
            //System.out.println(discount);
            totalPrice = totalPrice - (totalPrice * discount);
            jLabel24.setText(formato.format(totalPrice));
        }
        else jLabel24.setText(formato.format(totalPrice));
    }
    public void updateTable()
    {
        
        jPanel1.setLayout(new BorderLayout());
        JTable table = tablaArticulos.getTable();
        
        jPanel1.remove(scrollPane);
        scrollPane = new JScrollPane(table);
        jPanel1.add(scrollPane, BorderLayout.CENTER);
        jPanel1.repaint();
        DecimalFormat formato = new DecimalFormat("######.##");
        formato.setRoundingMode(RoundingMode.CEILING);
        jLabel20.setText(formato.format(tablaArticulos.getFactura().getPrecioTotal() - ivaAdded ));
        updatePrecios();
        //this.pack();
        
    }
